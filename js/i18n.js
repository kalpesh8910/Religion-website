  function normalizeLang(code) {
    return code === "gu" ? "gu" : "en";
  }

  function getLang() {
    try {
      return normalizeLang(localStorage.getItem(STORAGE_KEY) || "en");
    } catch (e) {
      return "en";
    }
  }

  function setStoredLang(code) {
    code = normalizeLang(code);
    try {
      localStorage.setItem(STORAGE_KEY, code);
    } catch (e) { }
    return code;
  }

  function applyDomLang(code) {
    code = normalizeLang(code);
    document.documentElement.lang = code === "gu" ? "gu" : "en";
    document.body.classList.toggle("lang-gu", code === "gu");
    return code;
  }

  function t(key) {
    var lang = getLang();
    var row = DICT[lang] || DICT.en;
    if (row[key] != null) return row[key];
    return DICT.en[key] != null ? DICT.en[key] : key;
  }

